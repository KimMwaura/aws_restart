#Defining a list

myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

#Accessing a list by position
print(myFruitList[2])
print(myFruitList[0])
print(myFruitList[1])

#Changing the values in a list
myFruitList [0] = "Orange"
print(myFruitList)

#Defining a tuple
myFinalAnswerTuple = ("apple", "banana", "pineapple")
print(myFinalAnswerTuple)

#Accessing a tuple by position
print(myFinalAnswerTuple[0])
print (myFinalAnswerTuple[2])
print(myFinalAnswerTuple[1])

#Introducing the dictionary data type
myFavoriteFruitDictonary = {
    "akua" : "apple",
    "saanki" : "banana",
    "paulo" : "pineapple"
}
print(myFavoriteFruitDictonary)

#Accessing a dictionary by name
print(myFavoriteFruitDictonary["akua"])
print(myFavoriteFruitDictonary["saanki"])
print(myFavoriteFruitDictonary["paulo"])
